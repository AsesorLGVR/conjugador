$(document).ready(function(){
$('#burbuja3').click(function(){
$('#frame3').html('<iframe width="640" height="800" src="abandonar.html" frameborder="0" allowfullscreen id="video"></iframe>');
$('#video3').css('display', 'block');
$('#video3').css('position', 'fixed');
$('#close3').css('position', 'fixed');
$('#close3').css('display', 'block');
$('#burbuja3').css('opacity', '1');
  $('header').css('opacity', '0');
  $('.main').css('opacity', '0');
});
});

$(document).ready(function(){
$('#close3').click(function(){
$('#frame3').html('');
$('#burbuja3').data('clicked', 'no');
$('#video3').css('display', 'none');
$('#close3').css('display', 'none');
$('#burbuja3').css('opacity', '1');
$('#video3').attr("src", "abandonar.html");
  $('header').css('opacity', '1');
  $('.main').css('opacity', '1');
});
});

